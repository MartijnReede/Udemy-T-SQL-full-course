/*
	SELECT staat gelijk aan "Print" in andere programmeertalen.

	SELECT 1 + 1 zal dus 2 printen.

	Alles zal geprint worden in een tabel, dit is namelijk een database. Als we willen dat de kolom een naam heeft
	kunnen we AS gebruiken. 

	SELECT 1 + 1 AS Result zal dus 2 printen in de kolom  "Result"

	GO zal een einde maken aan een "Batch". 
	Zoals je ziet bij Result2 en Result3 verschijnt er een + tekentje links van de SELECT statement. Deze horen bij dezelfde batch.
	Wil ik een nieuwe batch starten, dan moet ik na de GO keyword gewoon verder typen. 
*/

SELECT 1 + 1 AS Result
GO

SELECT 1 + 1 AS Result2
SELECT 1 + 2 AS Result3
GO


